public class ExemploContador {
    public static void main(String[] args) {
     int contador = 1 ;   
     System.out.println("Inciando o contador com do-while");
     do{
        System.out.println("O valor do contador é:  " + contador);
        contador++;
     } while(contador <=5);
     System.out.println("òtimo! você digitou o número positivo");
    }

    
}
